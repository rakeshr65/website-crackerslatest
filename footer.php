<script>
    // Function to get the current date in the format YYYY-MM-DD
    function getCurrentDate() {
        const now = new Date();
        const year = now.getFullYear();
        const month = String(now.getMonth() + 1).padStart(2, '0');
        const day = String(now.getDate()).padStart(2, '0');
        return `${year}-${month}-${day}`;
    }

    // Function to update the counters
    function updateCounters() {
        const currentDate = getCurrentDate();
        let yesterdayCount = localStorage.getItem('yesterdayCount') || 0;
        let todayCount = localStorage.getItem('todayCount') || 0;
        let monthlyCount = localStorage.getItem('monthlyCount') || 0;
        let totalCount = localStorage.getItem('totalCount') || 0;

        const lastVisitDate = localStorage.getItem('lastVisitDate');
        const lastMonth = localStorage.getItem('lastMonth');

        if (lastVisitDate !== currentDate) {
            yesterdayCount = todayCount;
            todayCount = 0;
            localStorage.setItem('yesterdayCount', yesterdayCount);
            localStorage.setItem('todayCount', todayCount);
        }

        if (lastMonth !== getCurrentDate().slice(0, 7)) {
            monthlyCount = 0;
            localStorage.setItem('monthlyCount', monthlyCount);
        }

        todayCount++;
        monthlyCount++;
        totalCount++;
        localStorage.setItem('todayCount', todayCount);
        localStorage.setItem('monthlyCount', monthlyCount);
        localStorage.setItem('totalCount', totalCount);
        localStorage.setItem('lastVisitDate', currentDate);
        localStorage.setItem('lastMonth', getCurrentDate().slice(0, 7));

        document.getElementById('yesterday-counter').innerHTML = `Yesterday's Visitors: ${yesterdayCount}`;
        document.getElementById('today-counter').innerHTML = `Today's Visitors: ${todayCount}`;
        document.getElementById('monthly-counter').innerHTML = `Monthly Visitors: ${monthlyCount}`;
        document.getElementById('total-counter').innerHTML = `Total Visitors: ${totalCount}`;
    }

    // Update counters when the page loads
    updateCounters();
</script>







